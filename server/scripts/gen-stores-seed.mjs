const buckets = {'>1M': 0, '500k-1M': 0, '200-500k': 0, '100-200k': 0, '<100k': 0};
for (const s of result) {
  if (s.population >= 1000000) buckets['>1M']++;
  else if (s.population >= 500000) buckets['500k-1M']++;
  else if (s.population >= 200000) buckets['200-500k']++;
  else if (s.population >= 100000) buckets['100-200k']++;
  else buckets['<100k']++;
}
